package TrabalhoBD;

/**
 *
 * @author Alex Barboza
 */
public class Universidade {
    private int codUniversidade;
    private String nomeUniversidade;
    private String cidadeUniversidade;
    private String estadoUniversidade;
    private String logotipoUniversidade;
    private String gritoDeGuerra;

    public Universidade() {
        
    }
    
    public int getCodUniversidade() {
        return codUniversidade;
    }

    public void setCodUniversidade(int codUniversidade) {
        this.codUniversidade = codUniversidade;
    }

    public String getNomeUniversidade() {
        return nomeUniversidade;
    }

    public void setNomeUniversidade(String nomeUniversidade) {
        this.nomeUniversidade = nomeUniversidade;
    }

    public String getCidadeUniversidade() {
        return cidadeUniversidade;
    }

    public void setCidadeUniversidade(String cidadeUniversidade) {
        this.cidadeUniversidade = cidadeUniversidade;
    }

    public String getEstadoUniversidade() {
        return estadoUniversidade;
    }

    public void setEstadoUniversidade(String estadoUniversidade) {
        this.estadoUniversidade = estadoUniversidade;
    }

    public String getLogotipoUniversidade() {
        return logotipoUniversidade;
    }

    public void setLogotipoUniversidade(String logotipoUniversidade) {
        this.logotipoUniversidade = logotipoUniversidade;
    }

    public String getGritoDeGuerra() {
        return gritoDeGuerra;
    }

    public void setGritoDeGuerra(String gritoDeGuerra) {
        this.gritoDeGuerra = gritoDeGuerra;
    }
    
}
